# 3. (程序题)编写程序，输入整数n（n>=0），分别利用for循环和while循环求n！。程序文件名为P3_3.py。 运行结果如下（键盘输入内容用下划线标注）：请输入非负整数n:5for循环:5!=120while循环:5!=120提示：请先选择正确的语言Python 3.x，然后将程序代码复制到答案区。

n = int(input("请输入非负整数n:"))

# for循环求n!
f = 1
for i in range(1, n + 1):
    f *= i
print("for循环:{}!={}".format(n, f))

# while循环求n!
f = 1
i = 1
while i <= n:
    f *= i
    i += 1
print("while循环:{}!={}".format(n, f))

