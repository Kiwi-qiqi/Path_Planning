# 1. 编写程序，计算区间[m,n]中的偶数之和（m，n从键盘输入，要求m，n都为整数且n>m）。程序文件名为P3_1.py。 运行结果如下（键盘输入内容用下划线标注）：请输入整数m:1请输入整数n(n>m):100m-n中所有偶数的和: 2550 请输入整数m:1请输入整数n(n>m):1000m-n中所有偶数的和:250500 提示：请先选择正确的语言Python 3.x，然后将程序代码复制到答案区。

m = int(input("请输入整数m:"))
n = int(input("请输入整数n(n>m):"))

sum = 0
for i in range(m, n + 1):
    if i % 2 == 0:
        sum += i

print("m-n中所有偶数的和:", sum)




